export class Products {

  id: number;
  name: string;
  description: string;
  price: number;
  imageURL: string;

  constructor(id: any, name: any, description: any, price: any, imageURL: any) {

    this.id = id;
    this.name = name;
    this.description = description;
    this.price = price;
    this.imageURL = imageURL;

  }

}

